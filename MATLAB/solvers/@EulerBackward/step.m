% 
% Step method for Euler backward class
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
function y = step(obj, ode_fun, y0)
%
%   Step forward in time
%
    f = @(y) y0 - y + obj.h * ode_fun(obj.t + obj.h, y);
    y = newton_raphson(@(y) f(y), y0);
end
