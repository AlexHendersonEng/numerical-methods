% log method of block class
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
function log(obj)
%
%   Log data to logger
%
    if obj.logging
        obj.logger.log(obj);
    end
end
