% 
% update method for Memory class
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
function update(obj, ~)
%
%   Input handling
%
    arguments
        obj Memory;
        ~;
    end
%
%   Update output
%
    obj.output = obj.prev_input;
    obj.prev_input = obj.input;
end
