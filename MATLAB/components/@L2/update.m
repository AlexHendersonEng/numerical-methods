%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 
% update method for L2 class
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
function update(obj, t)
%
%   Input handling
%
    arguments
        obj L2;
        t double;
    end
%
%   Update output
%
    obj.output = 0.5 .* (interp1(obj.y_t, obj.y, t) - obj.input) .^ 2;
end
