% 
% update method for L1 class
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
function update(obj, t)
%
%   Input handling
%
    arguments
        obj L1;
        t double;
    end
%
%   Update output
%
    obj.output = abs(interp1(obj.y_t, obj.y, t) - obj.input);
end
