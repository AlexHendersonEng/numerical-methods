% 
% log method of logger class
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
function log(obj, block)
%
%   Log data
%
    obj.t(obj.n, 1) = block.solver.t;
    obj.input(obj.n, :) = block.input;
    obj.output(obj.n, :) = block.output;
    obj.n = obj.n + 1;
end
