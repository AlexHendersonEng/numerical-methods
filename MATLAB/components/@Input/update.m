% 
% update method for Input class
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
function update(obj, t)
%
%   Input handling
%
    arguments
        obj Input;
        t double;
    end
%
%   Update output
%
    obj.output = Tensor(interp1(obj.u_t, obj.u, t, 'linear', 'extrap'));
end
